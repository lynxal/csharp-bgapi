public static class SlStatusExtensions
{
    public static string GetName(this SlStatus status) =>
        Enum.IsDefined(status) ? status.ToString() : "UnknownValue";

    public static SlStatus FromUint(uint value) =>
        Enum.IsDefined(typeof(SlStatus), value) ? (SlStatus)value : SlStatus.UnknownValue;
}
